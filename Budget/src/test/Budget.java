package test;

public class Budget {

	private String date;
	private String impression;
	private String visits;
	private String costs;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getImression() {
		return impression;
	}
	public void setImpression(String imression) {
		this.impression = imression;
	}
	public String getVisits() {
		return visits;
	}
	public void setVisits(String visits) {
		this.visits = visits;
	}
	public String getCosts() {
		return costs;
	}
	public void setCosts(String costs) {
		this.costs = costs;
	}
}
